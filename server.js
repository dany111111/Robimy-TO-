const express = require('express');
const app = express();
const PORT = process.env.PORT || 3000;

// Middleware
app.use(express.json());

// Prosta trasa
app.get('/', (req, res) => {
  res.send('Witaj w aplikacji Zróbmy To!');
});

// Uruchom serwer
app.listen(PORT, () => {
  console.log(`Serwer działa na porcie ${PORT}`);
});
